# 변수 선언(변수명 = 값)
a = 10
print(f'a = {a}')

# 동시에 여러 개의 변수를 선언할 때
a,b = 10,20
print(f'a = {a}, b = {b}')

# 변수 교환
a,b = b,a
print(f'a = {a}, b = {b}')

# 같은 값을 여러 개의 변수에 저장할 때
a = b = 10
print(f'a = {a}, b = {b}')

# 변수의 타입을 확인할 때
